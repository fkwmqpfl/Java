package com.yedam.java;

public class Exam01 {
	public static void main(String[] args) {
		int x = 10;
		double y= 2.0;
		
		//더하기
		int s=x+(int)y;
		System.out.println(s);
		
		//빼기
		int m=x-(int)y;
		System.out.println(m);
		
		//곱하기
		int mu=x*(int)y;
		System.out.println(mu);
		//나누기
		int d=x/(int)y;
		System.out.println(d);
	}
}
